package q1;

/**.
 * <p> This program makes an ASCII stick man using the
 * println method.</p>
 *
 * @author Leon Ho A00879122
 * @version 1.0
 */
public class Stickman {
    /**.
     * <p>This method prints the actual stick man</p>
     * @param args command line arguments.
     */
    public static void main(final String[] args) {
        System.out.println("    /\"\"\"\"\"\\");
        System.out.println("   | (o|o) |");
        System.out.println("   | '---' |");
        System.out.println("    \\_____/");
        System.out.println("   ___| |___");
        System.out.println("  / . '-' . \\");
        System.out.println("  |          |");
        System.out.println("  \\_|_____|_/");
        System.out.println("  /_|     |_\\");
        System.out.println("   |  .-.  |");
        System.out.println("   '._| |_.'");
        System.out.println("   /__| |__\\");
    }
}
